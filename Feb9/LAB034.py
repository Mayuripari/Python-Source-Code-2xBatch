# loops
# repeat a block of code multiple times
# range(start, stop, step)
for i in range(1, 10):  # it will print i from 0 to 9(10-1)
    print(i)

print("---------------------")
for i in range(5):  # start from 0 to 4
    print(i)
print("---------------------")
for i in range(1, 5):        # 1-4
    print(i)
print("---------------------")
for i in range(3, 5):        # 3-4
    print(i)
print("---------------------")
for i in range(1, 10, 2):   # 1-9 -> 1,3,5,7,9
    print(i)
    